int FastCos(int x)
{
  int sign = 1;
  if (x < 0) x = 1 - x;
  x = x & 1023;
  if (x > 512)
  {
    x -= 512;
    sign = -1;
  }
  if (x <= 256) return CosTable[x] * sign;
  return -CosTable[512 - x] * sign;
}

int FastSin(int x)
{
  int sign = 1;
  if (x < 0) x = 1 - x;
  x &= 1023;
  if (x > 512)
  {
    x -= 512;
    sign = -1;
  }
  if (x <= 256) return CosTable[256 - x] * sign;
  return CosTable[x - 256] * sign;
}

int FastSqrt(int x)
{
  int v_bit = 15;
  int n = 0;
  int b = 0x8000;

  if(x <= 1) return x;

  while(b)
  {
    int temp = ((n << 1) + b) << v_bit--;
    if(x >= temp)
    {
      n += b;
      x -= temp;
    }
    b >>= 1;
  }
  return n;
}

int Length2D(int x, int y)
{
  return FastSqrt(x * x + y * y);
}

int imin(int a, int b)
{
  return a < b ? a : b;
}

int imax(int a, int b)
{
  return a > b ? a : b;
}
